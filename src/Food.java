public class Food {
    String name;
    String category;
    double calories;
    double carbohydrates;
    double fat;
    double protein;

    public Food(String name, String category, double calories, double carbohydrates, double fat, double protein) {
        this.name = name;
        this.category = category;
        this.calories = calories;
        this.carbohydrates = carbohydrates;
        this.fat = fat;
        this.protein = protein;
    }
}
